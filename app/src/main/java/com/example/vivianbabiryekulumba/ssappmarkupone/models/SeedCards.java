package com.example.vivianbabiryekulumba.ssappmarkupone.models;

public class SeedCards {

    private int seedIcon;
    private String seedTitle;
    private String seedContent;

    public int getSeedIcon() {
        return seedIcon;
    }

    public void setSeedIcon(int seedIcon) {
        this.seedIcon = seedIcon;
    }

    public String getSeedTitle() {
        return seedTitle;
    }

    public void setSeedTitle(String seedTitle) {
        this.seedTitle = seedTitle;
    }

    public String getSeedContent() {
        return seedContent;
    }

    public void setSeedContent(String seedContent) {
        this.seedContent = seedContent;
    }

}
